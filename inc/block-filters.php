/**
 * Adds role="button" to .button elements, excluding .wp-block-button.
 */
add_filter( 'render_block', function ( $block_content, $block ) {
	if ( empty( $block_content ) ) {
		return $block_content;
	}

	if ( strpos( $block_content, 'class="' ) !== false && strpos( $block_content, 'button' ) !== false ) {
		$block_content = preg_replace_callback(
			'/<([a-z]+)([^>]*class=["\'][^"\']*\bbutton\b[^"\']*["\'][^>]*)>/i',
			function ( $matches ) {
				if ( strpos( $matches[2], 'wp-block-button' ) === false && strpos( $matches[2], 'role=' ) === false ) {
					return "<{$matches[1]}{$matches[2]} role=\"button\">";
				}
				return $matches[0];
			},
			$block_content
		);
	}

	return $block_content;
}, 10, 2 );

