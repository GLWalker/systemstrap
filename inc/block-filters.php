/**
 * Scans core/group blocks for specific classes and replaces <p class="close"> elements inside with <button> elements.
 */
add_filter( 'render_block', function ( $block_content, $block ) {
	if ( empty( $block_content ) || ( $block['blockName'] ?? '' ) !== 'core/group' ) {
		return $block_content;
	}

	$contexts = [ 'alert', 'modal', 'offcanvas', 'toast' ];

	foreach ( $contexts as $context ) {
		if ( strpos( $block_content, $context ) !== false ) {
			$block_content = preg_replace_callback(
				'/<([a-z1-6]+)([^>]*)class=["\']([^"\']*\bclose\b[^"\']*)["\']([^>]*)>(.*?)<\/\1>/i',
				function ( $matches ) use ( $context ) {
					$classes = esc_attr( $matches[3] );
					return sprintf(
						'<button class="%s" type="button" data-bs-dismiss="%s" aria-label="Close"></button>',
						$classes,
						esc_attr( $context )
					);
				},
				$block_content
			);
			break;
		}
	}

	return $block_content;
}, 10, 2 );

/**
 * Filter: Adds schema.org markup to core/site-logo blocks.
 */
add_filter( 'render_block_core/site-logo', 'strap_site_logo_block_filter', 10, 2 );
function strap_site_logo_block_filter( string $block_content, array $block ): string {
	if ( empty( $block_content ) ) {
		return $block_content;
	}

	$block_content = strap_replace_block_content(
		$block_content,
		'<img',
		'<img itemprop="logo"'
	);

	if ( strpos( $block_content, '<a ' ) !== false ) {
		$block_content = strap_replace_block_content(
			$block_content,
			'<a ',
			'<a itemscope itemtype="https://schema.org/ImageObject" '
		);
	} else {
		$block_content = strap_replace_block_content(
			$block_content,
			'<img',
			'<img itemscope itemtype="https://schema.org/ImageObject"'
		);
	}

	return $block_content;
}

